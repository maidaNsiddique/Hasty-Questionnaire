    public void checkFromDatabase()
    {
        for(int i=0;i<list.size();i++)
        {
            String query="Select *from mcqs where qn='"+Integer.parseInt(list1.get(i))+"'";
            try {
                pst=con.prepareStatement(query);
                 rs=pst.executeQuery();
                 if(rs.next())
                 {
                    int temp=rs.getInt("answer");
                    
                    if(temp==Integer.parseInt(list.get(i)))
                    {
                        //JOptionPane.showMessageDialog(null, "Your answer is right");
                        score+=1;
                    }
                 }
            } catch (SQLException ex) {
                Logger.getLogger(TakeQuiz.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            
        }
    }
