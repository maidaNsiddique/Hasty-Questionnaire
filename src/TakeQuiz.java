    public void getFromTable()
    {
        if(count !=8&& count<8)
        {
            int temp=Integer.parseInt(jTable1.getModel().getValueAt(count, 0).toString());
            String query="Select *from mcqs where qn='"+temp+"'";
            try {
                pst=con.prepareStatement(query);
                rs=pst.executeQuery();
                if(rs.next())
                {
                    jTextField1.setText(rs.getString("question"));
                    jRadioButton1.setText(rs.getString("option1"));
                    jRadioButton2.setText(rs.getString("option2"));
                    jRadioButton3.setText(rs.getString("option3"));
                    jRadioButton4.setText(rs.getString("option4"));
                    rs.close();
                    pst.close();   
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Not found");
                }
            } catch (SQLException ex) {
                Logger.getLogger(TakeQuiz.class.getName()).log(Level.SEVERE, null, ex);
            }
            
          
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No More Questions");
            jButton7.setEnabled(true);
        }
        
        
    }
